import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const pkg = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
const changelog = fs.readFileSync(path.join(root, 'CHANGELOG.md'), 'utf8');

function parseChangelog(markdown) {
  const entries = [];
  const lines = markdown.split('\n');
  let currentEntry = null;
  let currentGroup = null;

  for (const rawLine of lines) {
    const line = rawLine.trim();
    const versionMatch = line.match(/^##\s+v?\[?([^\s\]]+)\]?\s*(?:\(([^)]+)\))?/);
    if (versionMatch) {
      currentEntry = {
        version: versionMatch[1].trim(),
        date: (versionMatch[2] || '').trim(),
        changes: [],
      };
      entries.push(currentEntry);
      currentGroup = null;
      continue;
    }

    const groupMatch = line.match(/^###\s+(.+)/);
    if (groupMatch && currentEntry) {
      currentGroup = { category: groupMatch[1].trim(), items: [] };
      currentEntry.changes.push(currentGroup);
      continue;
    }

    if (line.startsWith('- ') && currentGroup) {
      currentGroup.items.push(line.slice(2).trim());
    }
  }

  for (const entry of entries) {
    entry.changes = entry.changes.filter((group) => group.items.length > 0);
  }

  return entries.filter((entry) => entry.changes.length > 0);
}

const outDir = path.join(root, 'src', 'generated');
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  path.join(outDir, 'versionInfo.ts'),
  `// This file is generated by scripts/generate-version-info.mjs. Do not edit by hand.\n` +
    `export const currentVersion = ${JSON.stringify(pkg.version)};\n` +
    `export const changelogData = ${JSON.stringify(parseChangelog(changelog), null, 2)} as const;\n`
);
